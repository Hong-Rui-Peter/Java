// 運算子
package Exercise;

public class Ex2_Operators {
    public static void main(String[] args) {
        int a = 3;

        System.out.println("Ex2_Operators:");
        System.out.println("a=3,--a:" + --a);
        System.out.println("a=3,a+=:" + (a += 3));
        System.out.println("a=3,++a:" + ++a);
        System.out.println("a=3,a-=:" + (a -= 5));
    }
}
